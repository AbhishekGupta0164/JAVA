public class operators {
    public static void main(String[] args) {
        int a = 4;
        int b = 6;
        System.out.println("The value of a + b is: " + (a + b));
        System.out.println("The value of a - b is: " + (a - b));
        System.out.println("The value of a * b is: " + (a * b));
        System.out.println("The value of a / b is: " + (a / b));
        System.out.println("The value of a % b is: " + (a % b)); // modulo operator
        System.out.println("The value of a++ is: " + (a++));
        System.out.println("The value of a-- is: " + (a--));
        System.out.println("The value of ++a is: " + (++a));
        System.out.println("The value of --a is: " + (--a));
        System.out.println(a > b); // comparison operators
        System.out.println(a < b);
        System.out.println(a > b && b > a);
        System.out.println(a > b || b > a); // logical operator OR
        System.out.println(a & b); // bitwise AND
        System.out.println(a | b); // bitwise OR

    }

}
